#Using class 
class Calculator:
    def add(x,y):
        return x+y
    def sub(x,y):
        return x-y
    def mul(x,y):
        return x*y
    def div(x,y):
        return x/y
#Creating object of class
print(Calculator.add(10,20))
print(Calculator.sub(20,10))
print(Calculator.mul(10,20))
print(Calculator.div(20,10))
